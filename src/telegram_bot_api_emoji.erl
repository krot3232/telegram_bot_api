-doc """
Generates a random emoji reaction from those available in Telegram.
""".
-spec random_reaction() -> Emoji :: binary().
random_reaction() ->
    Reaction = ?REACTIONS,
    lists:nth(rand:uniform(length(Reaction)), Reaction).

-doc """
Generates a random emoji for a forum topic.
""".
-spec random_topic() -> Emoji :: binary().
random_topic() ->
    Topic = ?TOPIC_EMOJI_IDS,
    lists:nth(rand:uniform(length(Topic)), Topic).

